def increase(n: float, value: float = 1) -> float:
    """

    :rtype: object
    """
    return n + n * value


def decrease(n: float, value: float = 1) -> float:
    return n - n * value


def double(n: float) -> float:
    return n * 2


def half(n: float) -> float:
    return n / 2
